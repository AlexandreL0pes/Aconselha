-- Exportação Disciplinas
SELECT DISCIPLINAS.* FROM DISCIPLINAS
INNER JOIN DISCIPLINAS_MATRIZES_CURRICULARES ON DISCIPLINAS.COD_DISCIPLINA = DISCIPLINAS_MATRIZES_CURRICULARES.COD_DISCIPLINA
INNER JOIN MATRIZES_CURRICULARES ON DISCIPLINAS_MATRIZES_CURRICULARES.COD_MATRIZ_CURRICULAR = MATRIZES_CURRICULARES.COD_MATRIZ_CURRICULAR
WHERE COD_CURSO = 80 OR COD_CURSO = 851 OR COD_CURSO = 862 ;

-- Exportação Disciplinas Matrizes Curriculares
