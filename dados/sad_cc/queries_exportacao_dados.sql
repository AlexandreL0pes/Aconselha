SELECT A.* FROM ALUNOS A
INNER JOIN MATRICULAS M ON A.COD_ALUNO = M.COD_ALUNO
WHERE M.COD_INSTITUICAO = 3 AND
      (M.COD_CURSO = 80 OR M.COD_CURSO = 851 OR M.COD_CURSO = 862);

